    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)cleanerRequestTable.getModel();
        
        model.setRowCount(0);
        try{
        for(WorkRequest request : cleanerOrganization.getWorkQueue().getWorkRequestList()){
            if(request instanceof CleaningWorkRequest){
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getMessage();
            row[2] = request.getSender().getCustomer().getName();
            row[3] = request.getReceiver()== null ? null : request.getReceiver().getEmployee().getName();          
            row[4] = request.getStatus();
            row[5]=request.getSender().getCustomer().getRoom().getRoomNo();
            model.addRow(row);
            }
        }
        }catch(Exception ex){
           JOptionPane.showMessageDialog(this, "Select a row", "Warning", JOptionPane.ERROR_MESSAGE); 
        }
    }

