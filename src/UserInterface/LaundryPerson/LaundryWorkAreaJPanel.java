    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = laundryTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Select a request", "Warning", JOptionPane.ERROR_MESSAGE);
            return;
        }

        WorkRequest request = (WorkRequest)laundryTable.getValueAt(selectedRow, 0);
        request.setReceiver(userAccount);
        request.setStatus("In Progress");
        JOptionPane.showMessageDialog(this, "This work is assigned to you", "Success", JOptionPane.WARNING_MESSAGE);
        populateTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed

        int selectedRow = laundryTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Select a request", "Warning", JOptionPane.ERROR_MESSAGE);
            return;
        }

        LaundryWorkRequest request = (LaundryWorkRequest)laundryTable.getValueAt(selectedRow, 0);
        request.setStatus("Processing");

        ProcessLaundryRequestJPanel processWorkRequestJPanel = new ProcessLaundryRequestJPanel(userProcessContainer, request);
        userProcessContainer.add("ProcessLaundryRequestJPanel", processWorkRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_processJButtonActionPerformed

