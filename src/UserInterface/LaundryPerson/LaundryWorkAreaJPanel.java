    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)laundryTable.getModel();
        
        model.setRowCount(0);
        try{
        for(WorkRequest request : attendantOrganization.getWorkQueue().getWorkRequestList()){
            if(request instanceof LaundryWorkRequest){
            Object[] row = new Object[7];
            row[0] = request;
            row[1] = request.getSender().getCustomer().getName();
            row[2] = request.getReceiver()== null ? null : request.getReceiver().getEmployee().getName();          
            row[3] = request.getMessage();
            row[4] = request.getStatus();
            LaundryWorkRequest laundryRequest=(LaundryWorkRequest)request;
            row[5]=laundryRequest.getResult();
            row[6]=request.getSender().getCustomer().getRoom().getRoomNo();
            
            model.addRow(row);
            }
        }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(this, "There is network issue", "Warning", JOptionPane.ERROR_MESSAGE);
        }
    }

