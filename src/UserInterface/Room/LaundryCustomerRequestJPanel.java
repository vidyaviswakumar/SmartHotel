    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) laundryTable.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
            if(request instanceof LaundryWorkRequest){
            Object[] row = new Object[7];
            row[0]=  request;            
            row[1] = request.getSender();
            row[2] = request.getReceiver();
            row[3] = request.getMessage();            
            row[4] = request.getStatus();
            String result = ((LaundryWorkRequest) request).getResult();
            row[5]=result;
            row[6]=request.getSender().getCustomer().getRoom().getRoomNo();
            model.addRow(row);
            }
        }
    }

